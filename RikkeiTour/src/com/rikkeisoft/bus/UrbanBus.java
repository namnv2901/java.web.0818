package com.rikkeisoft.bus;

public class UrbanBus extends Bus {
	private int turn;
	private double kilometerage;
	public int getTurn() {
		return turn;
	}
	public void setTurn(int turn) {
		this.turn = turn;
	}
	public double getKilometerage() {
		return kilometerage;
	}
	public void setKilometerage(double kilometerage) {
		this.kilometerage = kilometerage;
	}
	
	
	
}
