---b. Hi?n th? danh sách ?i?m c?a sinh viên theo môn h?c (mã sinh viên, h? và tên, môn thi, ?i?m thi)
SELECT SINHVIEN.MASV, SINHVIEN.HOTEN, MONHOC.TENMH, DIEM.DIEM
FROM SINHVIEN
LEFT JOIN DIEM ON SINHVIEN.IDSV = DIEM.IDSV
LEFT JOIN MONHOC ON DIEM.IDMH = MONHOC.IDMH
ORDER BY MONHOC.TENMH;

---c. Hi?n th? danh sách sv thi l?i theo môn h?c (?i?m<5) (mã sinh viên, h? và tên, môn thi, ?i?m thi)
SELECT SINHVIEN.MASV, SINHVIEN.HOTEN, MONHOC.TENMH, DIEM.DIEM
FROM SINHVIEN
LEFT JOIN DIEM ON SINHVIEN.IDSV = DIEM.IDSV
LEFT JOIN MONHOC ON DIEM.IDMH = MONHOC.IDMH
WHERE DIEM.DIEM < 5
ORDER BY MONHOC.TENMH;

---d. Hi?n th? danh sách sinh viên theo ?i?m gi?m d?n(mã sinh viên, h? và tên, môn thi, ?i?m thi)
