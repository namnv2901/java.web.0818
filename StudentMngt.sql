----e. Hi?n th? 10 sv có ?i?m cao nh?t(mã sinh viên, h? và tên, môn thi, ?i?m thi)
SELECT SINHVIEN.MASV, SINHVIEN.HOTEN, MONHOC.TENMH, DIEM.DIEM
FROM SINHVIEN
LEFT JOIN DIEM ON SINHVIEN.IDSV = DIEM.IDSV
LEFT JOIN MONHOC ON DIEM.IDMH = MONHOC.IDMH
WHERE ROWNUM <= 10
ORDER BY DIEM.DIEM DESC;

---f. Hi?n th? danh sách môn h?c và t?ng s? sinh viên thi môn(mã môn, tên môn, t?ng s? sinh viên)

SELECT MONHOC.IDMH, MONHOC.TENMH, COUNT(*) AS "TONG SO SINH VIEN"
FROM MONHOC
LEFT JOIN DIEM ON MONHOC.IDMH = DIEM.IDMH
GROUP BY MONHOC.IDMH, MONHOC.TENMH
ORDER BY MONHOC.IDMH;

---g. Hi?n th? danh sách môn h?c và t?ng s? sinh viên thi l?i(mã môn, tên môn, t?ng s? sinh viên thi l?i)
SELECT MONHOC.IDMH, MONHOC.TENMH, COUNT(*) AS "TONG SO SINH VIEN THI LAI"
FROM MONHOC
LEFT JOIN DIEM ON MONHOC.IDMH = DIEM.IDMH
WHERE DIEM.LANTHI > 1
GROUP BY MONHOC.IDMH, MONHOC.TENMH
ORDER BY MONHOC.IDMH;

---h. Danh sách sinh viên có ?i?m trung bình trên 7.0 (?i?m trung bình các môn thi) (Mã sinh viên, ?i?m trung bình)
SELECT SINHVIEN.MASV, AVG(DIEM.DIEM)
FROM SINHVIEN
LEFT JOIN DIEM ON SINHVIEN.IDSV = DIEM.IDSV
GROUP BY SINHVIEN.MASV
ORDER BY SINHVIEN.MASV;

